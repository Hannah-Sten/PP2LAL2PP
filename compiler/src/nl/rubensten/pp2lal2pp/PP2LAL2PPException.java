public class PP2LAL2PPException extends RuntimeException {

    public PP2LAL2PPException() {
        super();
    }

    public PP2LAL2PPException(String message) {
        super(message);
    }

    public PP2LAL2PPException(String message, Throwable cause) {
        super(message, cause);
    }

    public PP2LAL2PPException(Throwable cause) {
        super(cause);
    }

}
